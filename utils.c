#include "minitalk.h"

int	ft_atoi(const char *str)
{
	long		sign;
	long long	result;

	sign = 1;
	result = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == 45 && *str++)
		sign = -1;
	else if (*str == 43)
		str++;
	while (*str >= 48 && *str <= 57)
	{
		if (sign == -1 && result * 10 < result)
			return (0);
		else if (sign == 1 && result * 10 < result)
			return (-1);
		result = result * 10 + (*str++ - 48);
	}
	return (result * sign);
}

static void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		ft_putnbr_fd(147483648, fd);
	}
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
		ft_putnbr_fd(n, fd);
	}
	else if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
		ft_putchar_fd(n + 48, fd);
}
